/******************************************************************************
 * Vorzeichenbehaftete Typen
 ******************************************************************************/
typedef signed char         __s8;   ///< Vorzeichenbehafteter 8-Bit-Wert 
typedef signed short        __s16;  ///< Vorzeichenbehafteter 16-Bit-Wert 
typedef signed long         __s32;  ///< Vorzeichenbehafteter 32-Bit-Wert
//typedef signed long long    __s64; 	///< Vorzeichenbehafteter 64-Bit-Wert
/******************************************************************************/

/******************************************************************************
 * Vorzeichenlose Typen
 ******************************************************************************/
typedef unsigned char       __u8;   ///< Vorzeichenloser 8-Bit-Wert
typedef unsigned short      __u16;  ///< Vorzeichenloser 16-Bit-Wert
typedef unsigned long       __u32;  ///< Vorzeichenloser 32-Bit-Wert
//typedef unsigned long long  __u64; 	///< Vorzeichenloser 64-Bit-Wert
/******************************************************************************/

#endif //__MYTYPES_H__
